M.get_arrow = function()
  local arrows = {
    default = {
      left = '',
      right = ' ',
    },

    round = {
      left = '',
      right = '',
    },

    block = {
      left = '█',
      right = '█',
    },

    arrow = {
      left = '',
      right = '',
    },
  }

  local types = { 'default', 'round', 'block', 'arrow' }

  math.randomseed(os.time())
  local ind = math.random(1, #types)
  local name = types[ind]

  return arrows[name]
end

